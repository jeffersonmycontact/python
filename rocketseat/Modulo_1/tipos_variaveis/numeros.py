# Inteiro
numero_inteiro = 3
print("Inteiro = ", numero_inteiro)

# Real com ponto flutuante
numero_real = 3.14
print("Real com ponto flutuante = ", numero_real)

# type()
print("Tipo da variável inteiro = ", type(numero_inteiro))
print("Tipo da variável real = ", type(numero_real))

# soma +
soma = 1 + 1
print("1 + 1 = ", soma)

# subtração -
subtracao = 1 - 1
print("1 - 1 = ", subtracao)

# multiplicação *
multiplicacao = 2 * 2
print("2 * 2 = ", multiplicacao)

# divisão /
divisao = 5 / 2
print("5 / 2 = ", divisao)
print("Tipo da variável do resultado da divisão = ", type(divisao))

# int() converte de float para int
print("Valor em inteiro = ", int(divisao))

# float() coverte de int para float
print("Valor em float = ", float(divisao))

# módulo % mostra o resto da divisão
modulo = 5 % 2
print("Resto da divisão = ", modulo)

# divisão inteiro //
divisao = 5 // 2
print("5 / 2 = ", divisao)
print("Tipo da variável do resultado da divisão = ", type(divisao))